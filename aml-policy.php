                                <h1>6. Market Abuse</h1>
                                <h1>6.1 Introduction</h1>
                                <p>Market abuse relates to “behavior”
                                    (action or inaction) by a person acting alone or in a concert party, which occurs in
                                    relation to qualifying investments traded on, or admitted to trade on a prescribed
                                    market. There are 7 types of abusive behavior.</p>
                                <p>Although Tech Sport Global only offer OTC
                                    investment instruments and not exchange traded products, the act of market abuse
                                    also covers any relevant product of an investment traded in any of the above. As
                                    Tech Sport Global’s prices are derived from the movements in the underlying market, CFDs are
                                    caught within the market abuse regime.</p>
                                <h1>6.2 The 7 Types of Market Abuse
                                </h1>
                                <p>Insider Dealing – where an insider
                                    deals or attempts to deal in a qualifying or related investment on the basis of
                                    inside information.</p>
                                <p>Improper disclosure – where an
                                    insider discloses inside information to another person, other than in proper
                                    exercise of their employment.</p>
                                <p>Misuse of information – where
                                    behavior is based upon information not generally available but which is relevant and
                                    a “regular market user” would consider to be a failure to observe reasonable
                                    standards. (It is possible to misuse information which is not necessarily inside
                                    information).</p>
                                <p>Manipulating Transactions – where
                                    behavior consists of placing transactions or orders, other than in accordance with
                                    accepted market practice, which gives a false or misleading impression of
                                    demand/supply/prices/valuations.</p>
                                <p>Manipulating Devices – where
                                    behavior consists of placing transactions or orders which employ fictitious devices
                                    or other forms of deception..</p>
                                <p>Dissemination – where behavior
                                    consists of disseminating information, which is likely to give a false or misleading
                                    impression of an investment by a person who knew that the information was
                                    false/misleading.</p>
                                <p>Distortion – where behavior gives
                                    the regular user a false or misleading impression of demand/supply/price/value or
                                    where it would be considered a failure to observe standards of behavior expected by
                                    a person of their position</p>
                                <h1>6.3 Indications of Suspicious
                                    Transactions</h1>
                                <li>1.A client opens an account and
                                    immediately gives an order to conduct a significant transaction in a particular
                                    security, especially if the client is insistent that the order is carried out very
                                    urgently.</li>
                                <li>2.A transaction is significantly
                                    out of line with the client’s previous investment behavior e.g. type of security,
                                    amount invested, size of order</li>
                                <li>3.There is unusual trading in the
                                    shares of a company before the announcement of price sensitive information relating
                                    to the company</li>
                                <li>4.An employee’s own account
                                    transaction is timed just before clients’ transactions and related orders in the
                                    same financial instrument</li>
                                <li>5.An order will, because of its
                                    size in relation to the market in that security, clearly have a significant impact
                                    on the supply or demand for or the price or value of the security
                                This list is not exhaustive; it just provides some examples of what constitutes suspicious activity.</li>
                                <h1>6.4 Suspicious Transaction
                                    Reporting</h1>
                                <p>As with any other type of
                                    suspicious activity, it is very important that any suspicions relating to market
                                    abuse are reported to the AML/CTF reporting officer(or another member of Compliance)
                                    without delay.</p>
