                                <h1>2. Anti-Money Laundering (AML) –
                                    the requirements</h1>
                                <p>Globally, there has been a
                                    long-standing obligation to have effective procedures in place to detect and prevent
                                    money laundering. Specifically, Tech Sport Global is registered in St Vincents and the Grenadines
                                    and the Anti Money Laundering and Terrorist Financial regulations (2014) in St
                                    Vincents and the Grenadines place an obligation on the Firm to adopt suitable AML
                                    policies and procedures. Tech Sport Global is committed to following all global standards and
                                    applicable legislation and has implemented the policies and procedures referred to
                                    in the document to ensure we are able to detect and prevent money laundering taking
                                    place through the Firm.</p>
                                <h1>2.1 What is Money Laundering?</h1>
                                <p>Money laundering is the process by
                                    which criminals attempt to conceal the true origins and/or ownership of the
                                    proceeds, or benefits, of criminal activity. The aim of the process is to achieve a
                                    situation where the proceeds of their activities appear legitimate; enabling them to
                                    avoid prosecution, conviction and the confiscation of the criminal funds. Financial
                                    institutions are used for the purpose of money laundering and therefore employees
                                    within the sector must be aware of the regulatory requirements placed both upon the
                                    firm they work for and themselves as individuals.</p>
                                <h1>2.2 The Stages of Money Laundering
                                </h1>
                                <p>Money laundering is traditionally
                                    described as only having 3 basic stages but it is important to remember that these
                                    stages do not necessarily occur in every case. Also, the boundaries between them may
                                    be blurred or overlap. Money laundering takes many forms and the classic money
                                    laundering model does not always apply. For example, someone may handle the benefits
                                    of acquisitive crimes (such as theft, fraud or tax evasion) and still be caught by
                                    the money laundering definition. Nonetheless, the 3 classic stages of laundering are
                                    outlined briefly below:</p>
                                <p><b>Placement :</b> The physical
                                    introduction of cash or other proceeds into the financial system – for example, by
                                    depositing cash into a bank account.</p>
                                <p><b>Layering :</b> The process of moving
                                    the proceeds through a number of (sometimes complex) financial transactions, so as
                                    to make the audit trail more difficult to trace back.</p>
                                <p><b>Examples include:</b></p>
                                <ul>
                                    <li>selling one investment and reinvesting in another;</li>
                                    <li>moving the money between different institutions or jurisdictions; and</li>
                                    <li>setting up vehicles such as companies and trusts so as to change the apparent
                                        ownership of the assets.</li>
                                </ul>
                                <p>It is at the layering stage that
                                    Tech Sport Global is most exposed.</p>
                                <p><b>Integration:</b> This is the point at
                                    which the funds have been sufficiently distanced from their origin to have achieved
                                    the appearance of legitimacy and the money is integrated into the economy.</p>
                                <h1>2.3 Suspicious Activity</h1>
                                <h1>It is at the layering stage that
                                    Tech Sport Global is most at risk although employees should be vigilant for any suspicious
                                    behavior by clients. For example:</h1>
                                <ul>
                                    <li>trying to make cash payments into their account</li>
                                    <li>paying money into the account only to withdraw at a later date without any
                                        trading activity</li>
                                    <li>making deposits which exceed the amount they stated they had available for
                                        trading on their application forms</li>
                                    <li>Routing funds through third-party accounts;</li>
                                    <li>Trading without regard for the costs of the transactions;</li>
                                    <li>Extensive use of overseas accounts or excessive use of complex corporate
                                        structures;</li>
                                    <li>Funds in payment of a transaction arriving from an unusual source.</li>
                                    <li>A customer exhibits an unusual level of concern for secrecy, particularly with
                                        regard to the customer's identity, type of business or source of assets;</li>
                                    <li>A corporate customer lacks general knowledge of its own industry;</li>
                                    <li>A customer is unconcerned with risks, commissions or other costs associated with
                                        trading;</li>
                                    <li>A customer appears to be acting as an agent for another entity or individual but
                                        is evasive about the identity of the other entity or individual (except
                                        situations involving the identity of ownership interests in a collective
                                        investment vehicle);</li>
                                    <li>A customer is from, or has accounts in a country identified as, a haven for bank
                                        secrecy, money laundering or narcotics production;</li>
                                    <li>A customer engages in extensive, sudden or unexplained wire activity (especially
                                        wire transfers involving countries with bank secrecy laws);</li>
                                    <li>A customer makes a funds deposit followed by a request that the money be wired
                                        out or transferred to a third party, or to another firm, without any apparent
                                        business purpose.</li>
                                    <li>trading patterns which do not fit those ascertained from the trading experience
                                        section of their application forms</li>
                                </ul>
