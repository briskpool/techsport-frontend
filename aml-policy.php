                                <h1>3. Terrorist Financing</h1>
                                <h1>3.1 What is Terrorism?</h1>
                                <p>Terrorism as “the use or threat of
                                    action.... designed to influence the government or intimidate the public.... or the
                                    use or threat made for the purpose of advancing a political, religious or
                                    ideological cause” whereby there is:</p>
                                <ul>
                                    <li>Violence against a person</li>
                                    <li>Damage to property</li>
                                    <li>Serious risk to public health or safety</li>
                                    <li>Interference with an electronic system</li>
                                </ul>
                                <h1>3.2 How does it apply to Financial
                                    Institutions?</h1>
                                <p>The relevance to financial
                                    institutions is “terrorist property” and the laundering of such property. It is
                                    defined as:</p>
                                <ul>
                                    <li>Money or other property likely to be used for terrorism</li>
                                    <li>Proceeds of the commission of acts of terrorism</li>
                                    <li>Proceeds of acts carried out for the purposes of terrorism</li>
                                </ul>
                                <h1>3.3 Terrorist Financing VersusMoney Laundering</h1>
                                <p>There can be considerable
                                    similarities between the movement terrorist property and the laundering of criminal
                                    property: some terrorist groups are known to have well-established links with
                                    organised criminal activity. However, there are two major differences between
                                    terrorist property and criminal property:</p>
                                <ul>
                                    <li>Often only small amounts are required to commit individual terrorist acts, thus
                                        increasing the difficulty of tracking the terrorist property;</li>
                                    <li>Terrorists can be funded from legitimately obtained income, including charitable
                                        donations, and it is extremely difficult to identify the stage at which
                                        legitimate funds become terrorist property.</li>
                                </ul>
                                <p>Terrorist organisations can,
                                    however, require quite significant funding and property to resource their
                                    infrastructure. They often control property and funds from a variety of sources and
                                    employ modern techniques to manage these funds, and to move them between
                                    jurisdictions.</p>
                                <p>In combating terrorist financing,
                                    the obligation on Tech Sport Global is to report any suspicious activity to the authorities. This
                                    supports the aims of the law enforcement agencies in relation to the financing of
                                    terrorism by allowing the freezing of property where there are reasonable grounds
                                    for suspecting that such property could be used to finance terrorist activity.</p>
